public class Tema01Ejercicio10 {
  public static void main(String[] args) {
    System.out.printf("\033[32m%-10s", "       * \n");
    System.out.printf("%-10s", "      *** \n");
    System.out.printf("%-10s", "     ***** \n");
    System.out.printf("%-10s", "    ******* \n");
    System.out.printf("%-10s", "   ********* \n");
    System.out.printf("%-10s", "  ***********\n");
    System.out.printf("%-10s", " *************\n");
    System.out.printf("%-10s\033[33m", "***************\n");
    System.out.printf("%-10s", "     *****\n");
    System.out.printf("%-10s", "     *****\n");
    System.out.printf("%-10s", "     *****\n");
    System.out.printf("%-10s", "     *****\n");
    System.out.printf("%-10s", "     *****\n");
    System.out.printf("%-10s\033[92m", "     *****\n");
    System.out.printf("%-10s", "***************\n");
    System.out.printf("%-10s", "***************\n");
  }
}

