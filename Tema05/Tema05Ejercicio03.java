public class Tema05Ejercicio03 {
  public static void main(String[] args) {
    System.out.println("Programa que muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle do while: ");
    
    int i = 0;
    
    
    do{
      System.out.println(i);
      i += 5;
    }while (i <= 100);
    
  }
}
