public class Tema05Ejercicio04 {
  public static void main(String[] args) {
    System.out.println("Programa que muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando un bucle: ");
    for (int i = 320; i >= 160; i -= 20) {
      System.out.println(i);
    }
  }
}
